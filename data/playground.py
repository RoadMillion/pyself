def print_unique_numbers(arr1, arr2):
    # 将两个数组转换成集合
    set1 = set(arr1)
    set2 = set(arr2)

    # 找出两个集合的差集
    unique_in_arr1 = set1 - set2
    unique_in_arr2 = set2 - set1

    # 打印两个数组中独有的数字
    print("finance：", unique_in_arr1)
    print("acc：", unique_in_arr2)


